//Useful to get a possible room coming from direction
function getRoomsOpenToDirection(direction) {
    var validRooms = [];
    for(var i = 0; i < rooms.length; i++) {
        if((rooms[i].validEntrances & direction) == direction) {
            validRooms.push(rooms[i])
        }
    }

    return validRooms
}

